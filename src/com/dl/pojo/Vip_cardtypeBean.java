package com.dl.pojo;

public class Vip_cardtypeBean {

	private String id;
	private String descript;
	private String rmcode;
	private String poscode;
	private String exchangeRate;
	private String exchangeRateFlag;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDescript() {
		return descript;
	}
	public void setDescript(String descript) {
		this.descript = descript;
	}
	
	public String getRmcode() {
		return rmcode;
	}
	public void setRmcode(String rmcode) {
		this.rmcode = rmcode;
	}
	public String getPoscode() {
		return poscode;
	}
	public void setPoscode(String poscode) {
		this.poscode = poscode;
	}
	public String getExchangeRate() {
		return exchangeRate;
	}
	public void setExchangeRate(String exchangeRate) {
		this.exchangeRate = exchangeRate;
	}
	public String getExchangeRateFlag() {
		return exchangeRateFlag;
	}
	public void setExchangeRateFlag(String exchangeRateFlag) {
		this.exchangeRateFlag = exchangeRateFlag;
	}
	@Override
	public String toString() {
		return "Vip_cardtypeBean [descript=" + descript + ", exchangeRate="
				+ exchangeRate + ", exchangeRateFlag=" + exchangeRateFlag
				+ ", id=" + id + ", poscode=" + poscode + ", rmcode=" + rmcode
				+ "]";
	}
	
	
	
	
}
